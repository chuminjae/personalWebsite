function animation() {
  $(".man").each(function () {
    const $svg = $(this);
    const $face = $svg.find(".face-group");
    const $iceCream = $svg.find(".ice-cream");

    const tl = gsap.timeline({ repeat: -1 });

    tl.to($iceCream, {
      duration: 3,
      rotation: 30,
      scaleY: 1.1,
      skewX: "10deg",
      ease: "expo.in",
      transformOrigin: "20px 40px",
    });
    tl.to($iceCream, { duration: 1, ease: "expo.in", y: 465, rotation: 0 });
    tl.to($iceCream, { duration: 0.01, scaleX: 1.4, skewX: "10deg" });
    tl.to($face, {
      duration: 1,
      rotation: 30,
      transformOrigin: "70px 70px",
      y: 5,
    });

    tl.addLabel("melt");
    tl.to(
      $iceCream,
      { duration: 3, scaleX: 3, scaleY: 0.1, transformOrigin: "25px 42px" },
      "melt"
    );
    tl.to($iceCream, { duration: 2, delay: 1, opacity: 0 }, "melt");
    tl.to($iceCream, { duration: 0.01, y: -500 });
    tl.to($iceCream, {
      duration: 0.01,
      scaleX: 1,
      scaleY: 1,
      opacity: 1,
      skewX: 0,
      skewY: 0,
      x: 0,
      transformOrigin: "24px 42px",
    });

    tl.addLabel("fallLook");
    tl.to(
      $face,
      { duration: 2, rotation: 0, transformOrigin: "70px 70px", y: 0 },
      "fallLook"
    ).to($iceCream, { duration: 1, y: 0, ease: "expo.in" }, "fallLook");
  });
  let grid = document.querySelector(".grid");
  let masonry = new Masonry(grid);
  window.onload = () => {
    grid = document.querySelector(".grid");
    masonry = new Masonry(grid, {
      itemSelector: ".grid-item",
      columnWidth: ".grid-sizer",
      horizontalOrder: true,
      // center align
      fitWidth: true,
      // same as margin bottom
    });
  };
  imagesLoaded(grid, function () {
    masonry.layout();
  });
  window.addEventListener("resize", () => {
    masonry.layout();
  });
}
